function resolveShoeName(shoeName: string) {
  if (!shoeName || shoeName === "自定义") return "THERUIZ AURA 德训鞋";
  return `THERUIZ AURA ${shoeName}`;
}

export function generateSoftSeedingContent(input: SoftSeedingInput): SoftSeedingContent {
  const draft = topicDrafts[input.topic];
  const shoeLabel = resolveShoeName(input.shoeName);
  const seasonLine = input.season ? `当前季节参数：${input.season}` : "";

  return {
    topic: input.topic,
    titles: draft.titles,
    body: draft.body,
    imageSuggestions: draft.imageSuggestions,
    imagePrompt: `Image 2.0 提示词（已同步当前鞋款/场景参数）\n\n产品基准：${shoeLabel}\n${seasonLine}\n\n${input.imagePrompt}`.trim(),
    tags: draft.tags,
    note: draft.note
  };
}

export function formatSoftSeedingContent(content: SoftSeedingContent) {
  return [
    `# THERUIZ AURA 软种草｜${content.topic}`,
    "",
    "## 标题",
    ...content.titles.map((title, index) => `${index + 1}. ${title}`),
    "",
    "## 正文",
    content.body,
    "",
    "## 配图建议",
    ...content.imageSuggestions.map((suggestion, index) => `${index + 1}. ${suggestion}`),
    "",
    "## Image 2.0 提示词",
    content.imagePrompt,
    "",
    "## 标签",
    content.tags.join(" "),
    "",
    "## 内容逻辑",
    content.note
  ].join("\n");
}
